    private void reset(){
        ID_Pasien.setText("");
        Gejala_Penyakit.setText("");
        Diagnosa_Penyakit.setText("");
        Kode_Obat.setText("");
    }
        
    private void tampilData() {
        int row = tbl_datalab.getRowCount();
        for (int a = 0; a < row; a++) {
            model.removeRow(0);
        }
        try {
            Connection cn = DriverManager.getConnection("jdbc:mysql://localhost/rumahsakit", "root", "");
            ResultSet rs = cn.createStatement().executeQuery("SELECT * FROM data_lab");
            while (rs.next()) {
                String data[] = {rs.getString("ID_Pasien"), rs.getString("Gejala_Penyakit"), rs.getString("Diagnosa_Penyakit"), rs.getString("Kode_Obat")};
                model.addRow(data);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Menampilkan Data GAGAL" + e);
        }
    }
    
